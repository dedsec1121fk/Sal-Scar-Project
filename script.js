function qs(sel, root=document){ return root.querySelector(sel); }
function qsa(sel, root=document){ return Array.from(root.querySelectorAll(sel)); }

function getTheme(){ return localStorage.getItem("theme") || "dark"; }
function getLang(){ return localStorage.getItem("lang") || "en"; }

function applyTheme(theme, persist=true){
  document.documentElement.setAttribute("data-theme", theme);
  if(persist) localStorage.setItem("theme", theme);

  const icon = qs("#themeIcon");
  const btn = qs("#themeToggle");

  if(theme === "light"){
    if(icon) icon.textContent = "☀️";
    if(btn){
      btn.setAttribute("aria-label", "Switch to dark theme");
      btn.setAttribute("title", "Switch to dark theme");
    }
  }else{
    if(icon) icon.textContent = "🌙";
    if(btn){
      btn.setAttribute("aria-label", "Switch to light theme");
      btn.setAttribute("title", "Switch to light theme");
    }
  }
}

function applyLang(lang){
  const dict = i18n[lang] || i18n.en;
  document.documentElement.setAttribute("data-lang", lang);
  document.documentElement.lang = (lang === "id") ? "id" : "en";

  qsa("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if(!key) return;
    if(dict[key] !== undefined) el.textContent = dict[key];
  });


  // Update document title + social meta for better previews per language
  if(dict.meta_title) document.title = dict.meta_title;
  const md = document.querySelector('meta[name="description"]');
  if(md && dict.meta_desc) md.setAttribute("content", dict.meta_desc);

  const ogt = document.querySelector('meta[property="og:title"]');
  if(ogt && dict.meta_title) ogt.setAttribute("content", dict.meta_title);
  const ogd = document.querySelector('meta[property="og:description"]');
  if(ogd && (dict.meta_og_desc || dict.meta_desc)) ogd.setAttribute("content", dict.meta_og_desc || dict.meta_desc);

  const twt = document.querySelector('meta[name="twitter:title"]');
  if(twt && dict.meta_title) twt.setAttribute("content", dict.meta_title);
  const twd = document.querySelector('meta[name="twitter:description"]');
  if(twd && (dict.meta_og_desc || dict.meta_desc)) twd.setAttribute("content", dict.meta_og_desc || dict.meta_desc);

  qsa("[data-i18n-title]").forEach(el => {
    const key = el.getAttribute("data-i18n-title");
    if(!key) return;
    if(dict[key] !== undefined) el.setAttribute("title", dict[key]);
  });

  const langLabel = qs("#langLabel");
  if(langLabel) langLabel.textContent = (lang === "en") ? "Indonesia" : "English";
  localStorage.setItem("lang", lang);
}

async function copyText(text){
  if(navigator.clipboard && window.isSecureContext){
    await navigator.clipboard.writeText(text);
    return;
  }
  const ta = document.createElement("textarea");
  ta.value = text;
  ta.setAttribute("readonly", "");
  ta.style.position = "fixed";
  ta.style.left = "-9999px";
  document.body.appendChild(ta);
  ta.select();
  document.execCommand("copy");
  document.body.removeChild(ta);
}

let toastTimer = null;
function showToast(message){
  const toast = qs("#toast");
  if(!toast) return;
  toast.textContent = message;
  toast.classList.add("show");
  clearTimeout(toastTimer);
  toastTimer = setTimeout(() => toast.classList.remove("show"), 1600);
}

function setupCopyables(){
  qsa("[data-copy]").forEach(el => {
    const val = el.getAttribute("data-copy") || "";
    const handler = async () => {
      try{
        await copyText(val);
        const dict = i18n[getLang()] || i18n.en;
        showToast(dict.copied || "Copied");
      }catch{
        const dict = i18n[getLang()] || i18n.en;
        showToast(dict.copied || "Copied");
      }
    };

    el.addEventListener("click", handler);
    el.addEventListener("keydown", (e) => {
      if(e.key === "Enter" || e.key === " "){
        e.preventDefault();
        handler();
      }
    });
  });
}


async function fetchGitHubProjects(){
  const grid = qs("#projectsGrid");
  if(!grid) return;

  const wanted = ["session-burner","frida-bypass-automation","medusa"];

  try{
    const res = await fetch("https://api.github.com/users/sal-scar/repos?per_page=100", {
      headers: { "Accept": "application/vnd.github+json" }
    });
    if(!res.ok) throw new Error("GitHub API error");
    const repos = await res.json();

    const byName = new Map(repos.map(r => [r.name, r]));
    const cards = wanted
      .map(name => byName.get(name))
      .filter(Boolean)
      .map(r => ({
        name: r.name,
        url: r.html_url,
        desc: r.description || "",
        lang: r.language || "—",
        stars: typeof r.stargazers_count === "number" ? r.stargazers_count : 0,
        license: (r.license && r.license.spdx_id && r.license.spdx_id !== "NOASSERTION") ? r.license.spdx_id : "—"
      }));

    if(!cards.length) return;

    // Replace content (keep styling)
    const dict = i18n[getLang()] || i18n.en;
    grid.innerHTML = "";
    cards.forEach(c => {
      const el = document.createElement("article");
      el.className = "card project";
      el.innerHTML = `
        <div class="project-top">
          <h3 class="h3">${escapeHtml(c.name)}</h3>
          <span class="pill mono">${escapeHtml(c.lang)}</span>
        </div>
        <p class="muted">${escapeHtml(c.desc)}</p>
        <div class="project-meta mono">
          <span aria-label="Stars">★ ${c.stars}</span>
          <span>${escapeHtml(c.license)}</span>
        </div>
        <a class="textlink" href="${c.url}" target="_blank" rel="noreferrer noopener">${escapeHtml(dict.projects_view || "View on GitHub")}</a>
      `;
      grid.appendChild(el);
    });
  }catch{
    // fallback already in HTML
  }
}

function escapeHtml(str){
  return String(str)
    .replaceAll("&","&amp;")
    .replaceAll("<","&lt;")
    .replaceAll(">","&gt;")
    .replaceAll('"',"&quot;")
    .replaceAll("'","&#039;");
}


document.addEventListener("DOMContentLoaded", () => {
  const theme = getTheme();
  const lang = getLang();

  applyTheme(theme, false);
  applyLang(lang);
  setupCopyables();
  fetchGitHubProjects();

  const themeBtn = qs("#themeToggle");
  if(themeBtn){
    themeBtn.addEventListener("click", () => {
      const next = (getTheme() === "dark") ? "light" : "dark";
      applyTheme(next, true);
    });
  }

  const langBtn = qs("#langToggle");
  if(langBtn){
    langBtn.addEventListener("click", () => {
      const next = (getLang() === "en") ? "id" : "en";
      applyLang(next);
      fetchGitHubProjects();
    });
  }
});
